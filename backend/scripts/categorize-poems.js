async function run() {
  await mongoose.connect(MONGODB)
  const Poem = require('../src/models/Poem')
  let updated = 0, notFound = 0
  for (const { id, genre } of batches) {
    const result = await Poem.updateOne({ _id: id }, { $set: { genre } })
    if (result.matchedCount > 0) updated++
    else { notFound++; console.log('NOT FOUND:', id) }
  }
  console.log(`Done — updated: ${updated}, not found: ${notFound}`)
  await mongoose.disconnect()
}

run().catch(e => { console.error(e); process.exit(1) })
